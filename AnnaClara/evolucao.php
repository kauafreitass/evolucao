const carousel = document.querySelector('.carousel');
let currentIndex = 0;
const items = document.querySelectorAll('.carousel-item');
const totalItems = items.length;

function showNext() {
    currentIndex = (currentIndex + 1) % totalItems;
    updateCarouselPosition();
}

function showPrev() {
    currentIndex = (currentIndex - 1 + totalItems) % totalItems;
    updateCarouselPosition();
}

function updateCarouselPosition() {
    const offset = -currentIndex * 100;
    carousel.style.transform = `translateX(${offset}%)`;
}

// Botões de navegação
document.querySelector('.carousel-next').addEventListener('click', showNext);
document.querySelector('.carousel-prev').addEventListener('click', showPrev);

// Função de carrossel automático
setInterval(showNext, 5000); // 5000ms = 5 segundos
